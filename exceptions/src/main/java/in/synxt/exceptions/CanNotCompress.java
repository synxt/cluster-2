package in.synxt.exceptions;

public class CanNotCompress extends Exception {

	public CanNotCompress() {
		super();
	}

	public CanNotCompress(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public CanNotCompress(String message, Throwable cause) {
		super(message, cause);
	}

	public CanNotCompress(String message) {
		super(message);
	}

	public CanNotCompress(Throwable cause) {
		super(cause);
	}

}
