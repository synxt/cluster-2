package in.synxt.oop.types;

public class Student {
	public String name;
	public int age;
	public int subjectOneMarks;
	public int subjectTwoMarks;
	public int subjectThreeMarks;
	@Override
	public String toString() {
		return "Student [name=" + name + ", age=" + age + ", subjectOneMarks=" + subjectOneMarks + ", subjectTwoMarks="
				+ subjectTwoMarks + ", subjectThreeMarks=" + subjectThreeMarks + "]";
	}
	
}
